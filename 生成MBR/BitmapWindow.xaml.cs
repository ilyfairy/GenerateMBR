        public static int ColorToBin(Color color)
        {
            if (color == Color.FromRgb(0x00, 0x00, 0x00))
            {
                return 0x0;
            }
            else if (color == Color.FromRgb(0x00, 0x00, 0xAA))
            {
                return 0x1;
            }
            else if (color == Color.FromRgb(0x00, 0xAA, 0x00))
            {
                return 0x2;
            }
            else if (color == Color.FromRgb(0x00, 0xAA, 0xAA))
            {
                return 0x3;
            }
            else if (color == Color.FromRgb(0xAA, 0x00, 0x00))
            {
                return 0x4;
            }
            else if (color == Color.FromRgb(0x00, 0xAA, 0x00))
            {
                return 0x5;
            }
            else if (color == Color.FromRgb(0xAA, 0x55, 0x00))
            {
                return 0x6;
            }
            else if (color == Color.FromRgb(0xAA, 0xAA, 0xAA))
            {
                return 0x7;
            }
            else if (color == Color.FromRgb(0x55, 0x55, 0x55))
            {
                return 0x8;
            }
            else if (color == Color.FromRgb(0x55, 0x55, 0xFF))
            {
                return 0x9;
            }
            else if (color == Color.FromRgb(0x55, 0xFF, 0x55))
            {
                return 0xA;
            }
            else if (color == Color.FromRgb(0x55, 0xFF, 0xFF))
            {
                return 0xB;
            }
            else if (color == Color.FromRgb(0xFF, 0x55, 0x55))
            {
                return 0xC;
            }
            else if (color == Color.FromRgb(0xFF, 0x55, 0xFF))
            {
                return 0xD;
            }
            else if (color == Color.FromRgb(0xFF, 0xFF, 0x55))
            {
                return 0xE;
            }
            else if (color == Color.FromRgb(0xFF, 0xFF, 0xFF))
            {
                return 0xF;
            }

            return 0;
        }
